#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	a;
	char	*tab;

	a = 0;
	if (n != 0)
	{
		tab = (char *)s;
		while (a < n)
		{
			tab[a] = '\0';
			a++;
		}
	}
}
